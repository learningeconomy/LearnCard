#!/usr/bin/env bash
# =============================================================================
# Regenerate the shared Caddy edge proxy config based on active PR previews
#
# Scans all running Docker Compose projects matching "pr-*" and generates a
# Caddyfile with routes for each. Then reloads Caddy to pick up changes.
#
# Usage:
#   ./regenerate-caddyfile.sh [BASE_DOMAIN]
# =============================================================================
set -euo pipefail

BASE_DOMAIN="${1:-preview.learncard.com}"
WORKSPACE_DIR="$HOME/preview-workspace"
REPO_DIR="$WORKSPACE_DIR/LearnCard"
CADDYFILE="$REPO_DIR/preview/dynamic-caddyfile"

echo "Regenerating Caddyfile for active previews..."

# Start with an empty Caddyfile
cat > "$CADDYFILE" << 'HEADER'
# Auto-generated Caddyfile for PR preview environments
# DO NOT EDIT — regenerated by regenerate-caddyfile.sh
HEADER

FOUND=false

for PROJECT in $(docker compose ls --format json 2>/dev/null | jq -r '.[].Name' | grep '^pr-' | sort); do
    PR_NUM="${PROJECT#pr-}"
    PREVIEW_DOMAIN="pr-${PR_NUM}.${BASE_DOMAIN}"

    # Verify the app container is running
    APP_CONTAINER="${PROJECT}-app-1"
    if ! docker inspect "$APP_CONTAINER" &>/dev/null; then
        echo "  Skipping ${PROJECT}: app container not found"
        continue
    fi

    FOUND=true
    echo "  Adding route: ${PREVIEW_DOMAIN} → ${PROJECT}"

    # Container names follow the pattern: <project>-<service>-<replica>
    cat >> "$CADDYFILE" << ROUTE

${PREVIEW_DOMAIN} {
    # Brain service (strip /brain prefix)
    handle_path /brain/* {
        reverse_proxy ${PROJECT}-brain-1:4000
    }

    # Cloud service (strip /cloud prefix)
    handle_path /cloud/* {
        reverse_proxy ${PROJECT}-cloud-1:4100
    }

    # LCA API service (strip /lca-api prefix)
    handle_path /lca-api/* {
        reverse_proxy ${PROJECT}-api-1:5100
    }

    # Health check
    handle /preview-health {
        respond "OK" 200
    }

    # Frontend app (catch-all)
    handle {
        reverse_proxy ${PROJECT}-app-1:3000
    }

    encode gzip zstd

    log {
        output stdout
    }
}
ROUTE
done

if [ "$FOUND" = "false" ]; then
    # No active previews — add a placeholder so Caddy doesn't error on empty config
    cat >> "$CADDYFILE" << 'PLACEHOLDER'

:80 {
    respond "No active previews" 200
}
PLACEHOLDER
    echo "  No active previews found."
fi

# Reload Caddy
if docker inspect preview-caddy &>/dev/null; then
    echo "Reloading Caddy..."
    docker exec preview-caddy caddy reload --config /etc/caddy/Caddyfile 2>/dev/null || {
        echo "  Caddy reload failed — restarting container..."
        docker restart preview-caddy
    }
    echo "Caddy reloaded."
else
    echo "  Caddy container not running — it will pick up the config on next start."
fi
